#include "JsonLoader.h"

#include <json.hpp>
#include <fstream>
#include <cassert>

const std::string JsonLoader::kDefaultBaseDirectory = "Resources/levels/";
const std::string JsonLoader::kExtension = ".json";

LevelData* JsonLoader::LoadJsonFile(const std::string& fileName) {
	// 連結してフルパスを得る
	const std::string fullpath = kDefaultBaseDirectory + fileName + kExtension;

	// ファイルストリーム
	std::ifstream file;

	// ファイルを開く
	file.open(fullpath);
	// ファイルオープン失敗をチェック
	if (file.fail()) {
		assert(0);
	}

	// JSON文字列から解凍したデータ
	nlohmann::json deserialized;

	// 解凍
	file >> deserialized;

	// 正しいレベルデータファイルかチェック
	assert(deserialized.is_object());
	assert(deserialized.contains("name"));
	assert(deserialized["name"].is_string());

	// "name"を文字列として取得
	std::string name = deserialized["name"].get<std::string>();
	// 正しいレベルデータファイルかチェック
	assert(name.compare("scene") == 0);

	// レベルデータ格納用インスタンスを生成
	LevelData* levelData = new LevelData();


	// "objects"の全オブジェクトを走査
	for (nlohmann::json& object : deserialized["objects"]) {
		assert(object.contains("type"));

		// 種別を取得
		std::string type = object["type"].get<std::string>();

		// MESH
		if (type.compare("MESH") == 0) {
			// 要素追加
			levelData->JsonObjects.emplace_back(LevelData::ObjectData{});
			// 今追加した要素の参照を得る
			LevelData::ObjectData& objectData = levelData->JsonObjects.back();

			if (object.contains("file_name")) {
				// ファイル名
				objectData.fileName = object["file_name"];
			}

			// トランスフォームのパラメータ読み込み
			nlohmann::json& transform = object["transform"];
			// 平行移動
			objectData.translation.x = -(float)transform["translation"][0];
			objectData.translation.y = (float)transform["translation"][1];
			objectData.translation.z = (float)transform["translation"][2]; // -
			//objectData.translation.z = - objectData.translation.z;			
			// 回転角
			objectData.rotation.x = JsonLoader::RadConvert((float)transform["rotation"][2]); // -
			objectData.rotation.x = - objectData.rotation.x;
			objectData.rotation.y = JsonLoader::RadConvert((float)transform["rotation"][1]); // -
			objectData.rotation.y = - objectData.rotation.y;
			objectData.rotation.z = JsonLoader::RadConvert((float)transform["rotation"][0]);
			// スケーリング
			objectData.scaling.x = (float)transform["scaling"][0];
			objectData.scaling.y = (float)transform["scaling"][1];
			objectData.scaling.z = (float)transform["scaling"][2];

			// TODO: コライダーのパラメータ読み込み

		}

		// TODO: オブジェクト走査を再帰関数にまとめ、再帰呼出で枝を走査する
		if (object.contains("children")) {

		}
	}

	return levelData;
}

/// <summary>
/// 度数法 -> ラジアン
/// </summary>
float JsonLoader::RadConvert(float value) {
	float radValue;

	radValue = value * (3.141592f / 180);

	return radValue;
}